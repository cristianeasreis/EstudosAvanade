function soma(args) {
    let result  = 0
  for(let i = 0; i < args.length, i++;){
      result += args[i]
  }
  return result
}

function main() {
  const resultado = soma(8, 7);
  console.log(resultado);
}

function main() {
    const resultado = soma[5,8,15,22];
    console.log(resultado);
  }
main()

